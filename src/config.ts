// 监听配置变化,自动同步更新变量
export function onConfigChange(event: vscode.ConfigurationChangeEvent): void
{
    if (!event.affectsConfiguration(CONFIG_SECTION)) { return; }

    const freshCfg = vscode.workspace.getConfiguration(CONFIG_SECTION);

    // 项目级配置
    if (event.affectsConfiguration(`${CONFIG_SECTION}.defines`))
    {
        defines = freshCfg.get<string[]>('defines')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.linked_libraries`))
    {
        linked_libraries = freshCfg.get<string[]>('linked_libraries')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.programArgs`))
    {
        programArgs = freshCfg.get<string[]>('programArgs')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.compiler`))
    {
        compiler = freshCfg.get<string>('compiler')!;
        standardOption = compiler === 'msvc' ? `/std:${cppStandard}` : `-std=${cppStandard}`;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.buildMode`))
    {
        buildMode = freshCfg.get<string>('buildMode')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.standard`))
    {
        cppStandard = freshCfg.get<string>('standard')!;
        standardOption = compiler === 'msvc' ? `/std:${cppStandard}` : `-std=${cppStandard}`;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.outputDirectory`))
    {
        outputDirectory = freshCfg.get<string>('outputDirectory')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.treatWarningsAsErrors`))
    {
        treatWarningsAsErrors = freshCfg.get<boolean>('treatWarningsAsErrors')!;
    }

    // Toolchain configs
    if (event.affectsConfiguration(`${CONFIG_SECTION}.gcc`))
    {
        gppPath = freshCfg.get<string>('gcc.path')!;
        gccDebugOptions = freshCfg.get<string[]>('gcc.debug_options')!;
        gccReleaseOptions = freshCfg.get<string[]>('gcc.release_options')!;
        gccWarningsOptions = freshCfg.get<string[]>('gcc.warnings_options')!;
        gccSanitizersOptions = freshCfg.get<string[]>('gcc.sanitizers_options')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.clang`))
    {
        clangppPath = freshCfg.get<string>('clang.path')!;
        clangDebugOptions = freshCfg.get<string[]>('clang.debug_options')!;
        clangReleaseOptions = freshCfg.get<string[]>('clang.release_options')!;
        clangWarningsOptions = freshCfg.get<string[]>('clang.warnings_options')!;
        clangSanitizersOptions = freshCfg.get<string[]>('clang.sanitizers_options')!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.msvc`))
    {
        msvcPath = freshCfg.get<string>('msvc.path')!;
        msvcDebugOptions = freshCfg.get<string[]>('msvc.debug_options')!;
        msvcReleaseOptions = freshCfg.get<string[]>('msvc.release_options')!;
        msvcWarningsOptions = freshCfg.get<string[]>('msvc.warnings_options')!;
        msvcSanitizersOptions = freshCfg.get<string[]>('msvc.sanitizers_options')!;
    }

    // Global configs
    if (event.affectsConfiguration(`${CONFIG_SECTION}.compiler`))
    {
        globalCompiler = (freshCfg.inspect<string>('compiler')?.globalValue ?? freshCfg.get<string>('compiler'))!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.buildMode`))
    {
        globalBuildMode = (freshCfg.inspect<string>('buildMode')?.globalValue ?? freshCfg.get<string>('buildMode'))!;
    }
    if (event.affectsConfiguration(`${CONFIG_SECTION}.standard`))
    {
        globalCppStandard = (freshCfg.inspect<string>('standard')?.globalValue ?? freshCfg.get<string>('standard'))!;
    }

    // Rebuild compile command template after any config change
    buildCompileCommand();
}

