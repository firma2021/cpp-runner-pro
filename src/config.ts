function getToolchainConfig(comp: string): { path: string; debug_options: string[]; release_options: string[]; warnings_options: string[]; sanitizers_options: string[] }
{
    if (comp === 'gcc') { return { path: gppPath, debug_options: gccDebugOptions, release_options: gccReleaseOptions, warnings_options: gccWarningsOptions, sanitizers_options: gccSanitizersOptions }; }
    if (comp === 'clang') { return { path: clangppPath, debug_options: clangDebugOptions, release_options: clangReleaseOptions, warnings_options: clangWarningsOptions, sanitizers_options: clangSanitizersOptions }; }
    if (comp === 'msvc') { return { path: msvcPath, debug_options: msvcDebugOptions, release_options: msvcReleaseOptions, warnings_options: msvcWarningsOptions, sanitizers_options: msvcSanitizersOptions }; }
    return { path: '', debug_options: [], release_options: [], warnings_options: [], sanitizers_options: [] };
}

async function saveToolchain(comp: string): Promise<void>
{
    await cfg.update(comp, getToolchainConfig(comp), vscode.ConfigurationTarget.Workspace);
}

export async function setBuildModeOptions(comp: string, mode: string, options: string[]): Promise<void>
{
    if (comp === 'gcc') { if (mode === 'debug') { gccDebugOptions = options; } else { gccReleaseOptions = options; } }
    if (comp === 'clang') { if (mode === 'debug') { clangDebugOptions = options; } else { clangReleaseOptions = options; } }
    if (comp === 'msvc') { if (mode === 'debug') { msvcDebugOptions = options; } else { msvcReleaseOptions = options; } }
    await saveToolchain(comp);
}

