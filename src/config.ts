export async function updateSetting(key: string, value: any, scope: 'global' | 'workspace' | 'workspaceFolder' = 'workspace'): Promise<void>
{
    if (key === 'compiler') { compiler = value; }
    if (key === 'buildMode') { buildMode = value; }
    if (key === 'standard') { cppStandard = value; }
    if (key === 'compiler' || key === 'standard') { standardOption = compiler === 'msvc' ? `/std:${cppStandard}` : `-std=${cppStandard}`; }
    if (key === 'outputDirectory') { outputDirectory = value; }
    const target = scope === 'global' ? vscode.ConfigurationTarget.Global : scope === 'workspaceFolder' ? vscode.ConfigurationTarget.WorkspaceFolder : vscode.ConfigurationTarget.Workspace;
    await cfg.update(key, value, target);
}

export function buildCompileCommand(): void
{
    const isMSVC = compiler === 'msvc';
    const toolchain = getToolchainConfig(compiler);

    const modeOptions = (buildMode === 'debug' ? toolchain.debug_options : toolchain.release_options).join(' ');
    const definesStr = defines.length ? defines.map(v => isMSVC ? `/D${v}` : `-D${v}`).join(' ') : '';
    const linkerStr = linked_libraries.length ? linked_libraries.map(v => isMSVC ? `${v}.lib` : `-l${v}`).join(' ') : '';

    let sanitizersOptions = '';
    if (!isMSVC && buildMode === 'debug')
    {
        const list = Array.isArray(toolchain.sanitizers_options) ? toolchain.sanitizers_options as string[] : [];
        if (list.length)
        {
            sanitizersOptions = `-fsanitize=${list.join(',')} -fno-omit-frame-pointer -g`;
        }
    }

    const TEMPLATE = isMSVC ? MSVC_TEMPLATE : GCC_CLANG_TEMPLATE;

    compileCmd = TEMPLATE
        .replace('${compiler}', toolchain.path)
        .replace('${standard}', standardOption)
        .replace('${modeOptions}', modeOptions)
        .replace('${warnings_options}', (toolchain.warnings_options || []).join(' '))
        .replace('${defines}', definesStr)
        .replace('${sanitizers_options}', sanitizersOptions)
        .replace('${linked}', linkerStr)
        .replace(/\s+/g, ' ')
        .trim();
}

// Initialize compile command template on module load
